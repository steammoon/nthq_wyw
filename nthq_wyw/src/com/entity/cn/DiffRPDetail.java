package com.entity.cn;

public class DiffRPDetail {
	private String UUID;
	private String DiffRP;
	private String LineNum;
	private String CreateOn;
	private String CreateBy;
	private Double BLQty;
	private Double Price;
	public String getUUID() {
		return UUID;
	}
	public void setUUID(String uUID) {
		UUID = uUID;
	}
	public String getDiffRP() {
		return DiffRP;
	}
	public void setDiffRP(String diffRP) {
		DiffRP = diffRP;
	}
	public String getLineNum() {
		return LineNum;
	}
	public void setLineNum(String lineNum) {
		LineNum = lineNum;
	}
	public String getCreateOn() {
		return CreateOn;
	}
	public void setCreateOn(String createOn) {
		CreateOn = createOn;
	}
	public String getCreateBy() {
		return CreateBy;
	}
	public void setCreateBy(String createBy) {
		CreateBy = createBy;
	}
	public Double getBLQty() {
		return BLQty;
	}
	public void setBLQty(Double bLQty) {
		BLQty = bLQty;
	}
	public Double getPrice() {
		return Price;
	}
	public void setPrice(Double price) {
		Price = price;
	}
}
